<h4>Valoración Inicial de Trabajo Social</h4>
<p>Realizada partiendo de una entrevista abierta al beneficiario o en su defecto a su acompañante,
según sus características especiales lo permita, en la que se indaga antecedentes como su procedencia, 
información sobre su red familiar y/o de apoyo, situación escolar, y en ese momento le es socializado al niño,
niña, adolescente y mayor de 18 años que cuenta con el derecho a una llamada semanal a su red familiar y/o red 
de apoyo, y del mismo modo a una visita familiar y/o red de apoyo. <br><br>
Por otra parte, se lleva a cabo la verificación de sus derechos, en el cual se identifica si el niño,
niña o adolescente cuenta con afiliación a salud, documento de identidad acorde a su edad y vinculación
al sistema de educación regular. En ese sentido, si se identifica que alguno de estos derechos se encuentra
vulnerado o si no se corrobora mediante la existencia física de algún documento soporte, se procederá a reportar 
y solicitar mediante oficio escrito a la autoridad administrativa competente la garantía del derecho respectivo. <br><br>
En el caso de contar con niños, niñas, adolescentes y mayores de 18 años pertenecientes a grupos étnicos,
se indaga por sus referentes de autoridades tradicionales; usos y costumbres que puedan orientar el plan 
de atención integral. Complementariamente se realiza la apertura de la carpeta historia de atención
de cada beneficiario que ingresa.
</p>
