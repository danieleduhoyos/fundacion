<h3>Ingreso</h3><br>    
    <p>Los niños, niñas, adolescentes y mayores de 18 años en situación de discapacidad mental
    y psicosocial ingresan bajo medida de restablecimiento de derechos y adoptabilidad por su condición
    de amenaza, inobservancia y vulneración de sus derechos fundamentales. <br><br>
    En esta fase, el objetivo de la Fundación Salvando Sueños es brindarles las bases para su futuro desempeño,
    abarcando todas las etapas del desarrollo perceptivo, motriz, lingüístico y hábitos de independencia personal, 
    ubicándolos en un ambiente acogedor donde se brinden pautas de autocuidado, entrenamiento en actividades básicas
    de su estilo de vida, relaciones interpersonales positivas y afectivas con sus semejantes, familias,
    terapeutas y demás personal perteneciente o visitante de la Fundación, generándole reconocimiento del 
    personal como figuras de autoridad, encaminados a formar en la convivencia diversas costumbres en sus medios 
    familiares y/o sociales. <br><br>
    Inicialmente se realiza un proceso de acogida y adaptación de los beneficiarios por medio del 
    acompañamiento del equipo interdisciplinario, realizando diversas intervenciones terapéuticas,
    psico – orientaciones y acompañamientos pedagógicos en el aula de clases; así mismo que la realización 
    de las valoraciones iniciales de los diferentes profesionales que ofrecen sus servicios en la modalidad de atención,
    la realización del estudio de caso de cada beneficiario, para posteriormente realizar un Diagnóstico Integral, 
    basado en la información oportuna y veraz de las defensorías de familia y la valoración individual de las diferentes 
    áreas de atención, con su adecuada ubicación en el respectivo nivel para el tratamiento, rehabilitación y cuidado; 
    todo ello dentro de un espacio de acogida y calidez. Durante esta fase, se desarrollan las siguientes actividades: <br> 
    </p>
