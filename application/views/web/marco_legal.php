    <h3>Marco Legal</h3><br>
    <h4>Marco Legal Internacional</h4>
    <p><b>Declaración Universal De Los Derechos Humanos:</b> adoptada en 1948 en la asamblea general de las naciones unidas.
    Programa De Acción Mundial Para Las Naciones Unidas: señala objetivos que tienden a una concepción integral 
    para la comprensión y manejo de la discapacidad, presentando un marco con definiciones conceptuales y fijando
    acciones en los campos de la prevención, la rehabilitación y la equiparación de oportunidades. <br><br>
    <b>Convención Sobre Los Derechos Del Niño:</b> En su artículo 23 contiene disposiciones sobre los derechos
    de y los deberes para con los niños con impedimento físico y mental, quienes deben tener un derecho a acceder
    a cuidados y atención especiales para alcanzar el disfrute de una vida digna y plena. Igualmente,
    en los artículos 24 al 28 se plantea, entre otros, los derechos a la atención en salud y los servicios de
    tratamiento y rehabilitación; especial cuidado; la seguridad social; un nivel de vida adecuado para 
    su desarrollo físico, mental, espiritual, moral y social; y en la educación.
    </p>
    <h4>Marco Legal Nacional</h4>
    <p>La Constitución Política de Colombia de 1991 incorporó como norma fundamental: <br><br>
    Artículo: 13 <i>"El Estado protegerá especialmente a aquellas personas que por su condición económica,
    física o mental, se encuentren en circunstancia de debilidad manifiesta y sancionará los abusos o maltratos 
    que contra ellas se cometan."</i><br><br>
    Artículo 16: <i>"Todas las personas tienen derecho al libre desarrollo de su personalidad sin más limitaciones
    que las que imponen los derechos de los demás y el orden jurídico."</i><br><br>
    Artículo 47: <i>"El Estado adelantará una política de previsión, rehabilitación e integración social para los
    disminuidos físicos, sensoriales y psíquicos, a quienes se prestará la atención especializada que requieran."</i><br><br>
    Artículo 52: Fija el derecho de todas las personas a la recreación y el deporte. <br><br>
    Artículo 68: <i>"La erradicación del analfabetismo y la educación de personas con limitaciones físicas o mentales, o con capacidades excepcionales,
    son obligaciones especiales del Estado."</i>
    </p>
    <h4>Disposiciones Legales Generales</h4>
    <p>Ley 75 de 1968: por la cual se dictan normas sobre filiación y se crea el Instituto de Bienestar Familiar.<br><br>
    En el artículo 26: <i>"El Instituto de Bienestar familiar, cuidara de que los menores no colocados bajo
    patria potestad, o guarda, estén bajo atención inmediata de las personas o establecimientos mejor
    indicados para, ello teniendo en cuenta la edad y demás condiciones del menor…"</i><br><br>
    Ley 7 de 1979: Por la cual se dictan normas para la protección de la niñez, 
    se establece el Sistema Nacional del Bienestar Familiar, se reorganiza el Instituto Colombiano
    del Bienestar Familiar y se dictan otras disposiciones. <br><br>
    DECLARACIÓN DE SALAMANCA, 1994 Normas Uniformes sobre la Igualdad de Oportunidades para las
    Personas con Discapacidad, El principio rector de este Marco de Acción es que las escuelas deben 
    acoger a todos los niños, independientemente de sus condiciones físicas, intelectuales, sociales,
    emocionales, lingüísticas u otras. <br><br>
    Ley 115 de 1994: Por la cual se expide la ley general de Educación TITULO III Modalidades de atención
    educativa a poblaciones CAPITULO 1 Educación para personas con limitaciones o capacidades excepcionales.<br><br>
    Artículo 46. Integración con el servicio educativo. La educación para personas con limitaciones físicas, 
    sensoriales, psíquicas, cognoscitivas, emocionales o con capacidades intelectuales excepcionales,
    es parte integrante del servicio público educativo. <br><br>
    Artículo 48. Aulas especializadas. Los Gobiernos Nacional, y de las entidades territoriales incorporarán
    en sus planes de desarrollo, programas de apoyo pedagógico que permitan cubrir la atención educativa a las
    personas con limitaciones. <br><br>
    Artículo 49. Alumnos con capacidades excepcionales. El Gobierno Nacional facilitará en los establecimientos
    educativos la organización de programas para la detección temprana de los alumnos con capacidades
    o talentos excepcionales y los ajustes curriculares necesarios que permitan su formación integral. <br><br>
    Ley 181 de 1995: Por la cual se dictan disposiciones para el fomento del Deporte, la recreación,
    el Aprovechamiento del Tiempo Libre y la Educación Física…”.
    El numeral 4 del artículo 3 plantea como parte del objeto, “Formular y Ejecutar Programas Especiales
    para la educación Física, deporte y recreación de las personas con discapacidades físicas, síquica,
    sensoriales…” lo cual es también tratado en los artículos 11, 12 y 24.<br><br>
    Decreto 2082 de 1996 Por el cual se reglamenta la atención educativa para personas
    con limitaciones o con capacidades o talentos excepcionales. <br><br>
    Ley 1098 de 2006: Por el cual se expide el código de la Infancia y Adolescencia. <br><br>
    Ley 1306 de 2009: Por medio de la cual se crea las normas para la protección de personas
    con discapacidad mental y se establece el régimen de la representación legal de incapaces emancipados. <br><br>
    Ley 1438 de 2011: por medio de la cual se reforma el sistema general de Seguridad Social en Salud. 
    Artículo 3, numeral 6: <i>"El principio de enfoque diferencial reconoce que hay poblaciones con características
    particulares en razón de su edad, género, raza, etnia, condición de discapacidad y víctimas de la violencia para
    las cuales el Sistema General de Seguridad Social en salud ofrecerá especiales garantías y esfuerzos encaminados
    a la eliminación de las situaciones de discriminación y marginación."</i><br><br>
    Ley 1616 de 2013: Por medio del cual se expide la ley de Salud Mental y se dictan otras disposiciones. <br><br>
    Ley Estatutaria 1618 de 2013: Por medio de la cual se establece las disposiciones para garantizar el pleno ejercicio
    de los derechos de las personas con discapacidad.  Se busca garantizar y asegurar el ejercicio efectivo de los derechos
    de las personas en condición de discapacidad mediante la adopción de medidas y políticas que cuenten con enfoque de inclusión,
    lo que le permitirá eliminar toda forma de discriminación en el país por cuenta de una condición. <br><br>
    Ley 1620 de 2013: Por la cual se crea el Sistema Nacional de Convivencia Escolar y Formación para el Ejercicio
    de los Derechos Humanos, la Educación para la Sexualidad y la Prevención y Mitigación de la Violencia Escolar. <br><br>
    Plan Nacional Decenal De Educación 2006  2016 en el tema Población con Necesidades Educativas Especiales Los siguientes
    apartes han sido extraídos del documento final del Plan Nacional Decenal de Educación 2006 – 2016 y corresponden a
    los puntos definidos para el tema de poblaciones con necesidades educativas especiales. El sistema educativo debe
    garantizar a niñas, niños, jóvenes y adultos, el respeto a la diversidad de su etnia, género, opción sexual, discapacidad,
    excepcionalidad, edad, credo, desplazamiento, reclusión, reinserción o desvinculación social y generar condiciones de atención
    especial a las poblaciones que lo requieran. <br><br>
    Por tal razón, la Fundación Salvando Sueños tratará de llevar a cabo el cumplimiento de las normas y disposiciones
    planteadas para el ejercicio de las funciones realizadas en este PAI, con el fin de mejorar la calidad de vida y garantizar
    los derechos, propiciar la inclusión de los niños, niñas, adolescentes y mayores de dieciocho años de esta fundación.
    </p>
