<h4>Valoración gestionada por el área de enfermería</h4><p>Desde el área de enfermería se inicia con el proceso
para ser valorado por los profesionales de la salud, medicina general, psiquiatría, odontología y nutrición, 
se procede a realizar la descripción del tratamiento médico a seguir y recomendaciones generales;
los datos personales se registran en el Kardex de enfermería, se realiza la respectiva nota de ingreso de 
enfermería y se realizan las tarjetas de medicamentos con sus respectivos tratamientos farmacológicos.
