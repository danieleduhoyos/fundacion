    <h4 id="interes">Interés Superior</h4>
    <p>Los niños(as), adolescentes y mayores de 18 años de la  Fundación salvando sueños,
    se basa en los conjuntos de acciones y procesos destinados a garantizar un  desarrollo integral y una vida digna,
    así como las condiciones materiales  y afectivas que les permitan vivir plenamente y alcanzar un óptimo desarrollo
    dentro de su diario vivir; por otra parte estos intereses deben ser reales y acordes a sus necesidades particulares
    y a sus aptitudes físicas y psicológicas, deben ser independientes del criterio arbitrario de los demás, es decir 
    que su existencia y protección no puede depender de la voluntad o capricho de los padres o de los empleados encargados
    de protegerlo.<br><br> 
    Desde la Fundación Salvan Sueños procura que los derechos de los niños, niñas, adolescentes y mayores de 18 años
    del programa sean garantizados o restablecidos, desde la vinculación al sistema educativo, al sistema de salud,
    a la recreación, a la libertad de expresión, a la participación, a la protección, al cuidado, a la identidad,
    a los servicios preferentes y a la de tener una familia. Si en algún momento determinado alguno de sus derechos
    está siendo vulnerado se informa de manera inmediata al defensor de familia para realizar las acciones pertinentes 
    según el caso.   De igual forma, se le socializa y se direcciona a la familia sobre el ejercicio de sus derechos y
    sobre las rutas de atención para salvaguardar los derechos de sus hijos.<br><br>
    Se gestiona directamente con el sector salud y el educativo la vinculación de los niños, niñas o adolescentes
    a estos servicios, garantizando de esta forma sus derechos fundamentales.  Algunos de esos derechos no garantizados,
    son identificados por los profesionales del programa a la hora de realizar el ingreso del participante a la modalidad,
    por medio de la obtención de sus documentos legales, otros derechos no garantizafos,  son verificados por medio 
    de las entrevistas psicológicas, familiares, pedagógicas y seguimientos a nivel personal, familiar, social y educativo. <br><br> 
    Así mismo, al interior de la Institución se garantiza la protección integral a los niños, niñas, adolescentes
    y mayores de 18 años, mediante estrategias de intervención implementadas bajo los programas de formación como
    vida saludable, desarrollo de potencialidades, construcción de ciudadanía, fortalecimiento individual y familiar 
    en cada una de las fases de atención, que buscan garantizar el desarrollo integral, pleno y armónico de los
    beneficiarios del programa contribuyendo a un bienestar físico, emocional, escolar  y social.
</p>
<h4 id="dignidad">Dignidad Humana</h4>
<p>Los niños, niñas, adolescentes y mayores de 18 años deben ser respetados y valorados como seres en lo 
    individual y lo social, con sus características y condiciones particulares, cada uno pertenece a un etnia,
    sexo, religión, ideologías y determinados rasgos físicos (alto, bajo, rubio, moreno, blanco, robusto, flaco, etc), 
    serán tratados como seres dignos y como sujetos de derechos; la dignidad humana
    entendida como autonomía o como posibilidad de diseñar un plan vital y de determinarse según
    sus características (vivir como quiera); La dignidad humana entendida como ciertas condiciones materiales 
    concretas de existencia (vivir bien); La dignidad humana entendida como intangibilidad de los bienes no patrimoniales,
    integridad física e integridad moral (vivir sin humillaciones).<br><br>
    En ese sentido, la atención a cada niño, niña y adolescente y mayor de 18 años ,
    familia y/o red vincular de apoyo, se realizará teniendo en cuenta que cada uno es un 
    ser humano que debe ser respetado y valorado como ser individual y social, de acuerdo a sus
    características y condiciones particulares. <br><br>
    A través de las diversas estrategias de atención se motiva a los niños, niñas, adolescentes y mayores
    de 18 años a pensar, sentir y actuar sin discriminar a otros por su etnia, orientación sexual, género
    o discapacidad.  Así mismo, se sensibiliza a la población frente a los recursos propios y desarrollo de
    habilidades para la toma de decisiones, asertividad, autonomía, reconocimiento de sus derechos y ejercicio
    de sus libertades estableciendo un proyecto de vida digno y que contribuya a una mejor calidad de vida.
</p>
<h4 id="igualdad">Igualdad y no discriminación </h4>
<p>Los niños, niñas, adolescentes y mayores de 18 años serán tratados, protegidos y cuidados de la misma manera,
    no serán víctimas de actos discriminatorios por motivos. Sin embargo, cuando se trata de personas que se 
    encuentran en condiciones de desigualdad (como por ejemplo las personas en condición de desplazamiento, 
    aquellas con discapacidad, las víctimas del conflicto armado, las minorías étnicas y raciales,
    entre otras poblaciones), darles un trato diferencial y positivo, es no solo válido sino una obligación
    para ayudarlas a superar los obstáculos que impiden su participación social. Esto se realiza a través de la
    implementación de un enfoque diferencial que contribuye a disminuir sus dificultades haciendo uso de acciones
    afirmativas. <br><br>
    Además, para lograr la realización del derecho a la igualdad de personas en situación de desigualdad,
    es fundamental reconocer las capacidades, potencialidades y recursos de estas poblaciones para garantizar 
    un verdadero trato igualitario y su inclusión social, para lograr la equidad. <br><br>
    Por esta razón, cada una de las acciones desarrolladas por el equipo profesional
    de la Fundación Salvando Sueños demanda un trato igualitario y libre de toda discriminación
    hacia los participantes directos del programa, sus familias y/o redes vinculares de apoyo. <br><br>
    Por lo tanto, desde la Fundación Salvando Sueños, se han implementado procesos pedagógicos y educativos
    que buscan sensibilizar a la población sobre el ejercicio de sus derechos, es decir que ellos identifiquen
    cuando sus derechos están siendo vulnerados y como pueden hacer valer estos derechos ante las demás personas 
    e instituciones.  También es importante mencionar que se trabaja con los niños, niñas, adolescentes, y mayores
    de 18 años diferentes temáticas que apunten hacia el reconocimiento de su identidad de género, su sexualidad, 
    sus capacidades, fortalezas y habilidades que les permita superar obstáculos de acuerdo con su contexto familiar,
    comunitario y social. <br><br>
    En los encuentros grupales se propician espacios pedagógicos donde se adquieren aprendizajes como aceptación
    de la diversidad, vivencia cotidiana de los valores humanos, promoviendo conductas proactivas positivas para
    una sana convivencia, se reflexiona frente al reconocimiento de las demás personas como sujetos de derechos 
    que merecen la misma atención, donde todos los seres humanos merecen respeto y mejorar constantemente. 
    Así mismo se orienta sobre factores protectores, se genera una cultura del respeto y se reflexiona sobre
    la identificación de capacidades, potencialidades y recursos de las diversas poblaciones para garantizar equidad
    e inclusión social.
</p>
<h4 id="oportunidad">Oportunidad</h4>
<p>Este principio posibilita que los niños, las niñas, adolescentes y mayores de 18 años, 
    con sus derechos inobservados, amenazados o vulnerados y sus familias y/o redes vinculares de apoyo,
    tengan acceso a la atención en forma eficaz en el momento requerido. Para ello, el talento humano tanto de
    los equipos técnicos interdisciplinarios de las Autoridades Administrativas Competentes como de los operadores 
    responsables del proceso de atención, desarrollen de manera ética y pertinente sus actuaciones en dichos procesos. <br><br> 
    Cada uno de los profesionales del equipo interdisciplinario y personal de la Fundación Salvando Sueños,
    se comprometen a ejecutar de manera ética y pertinente el desarrollo de sus acciones, que garanticen
    la atención de los niños, niñas y adolescentes, sus familias y/o redes vinculares de apoyo, de manera
    oportuna y eficaz. <br><br>
    En el proceso de atención con los niños, niñas, adolescentes y redes vinculares se brinda acompañamiento
    de manera eficaz en el momento que se requiera contribuyendo a los procesos íntegros, fiables y éticos.
    Con el conocimiento de cada situación particular de los beneficiarios se fortalecen el cumplimiento de 
    objetivos donde se brindan oportunidades de aprendizajes significativos para el proceso de restablecimiento 
    de derechos.
</p>
<h4 id="integralidad">Integralidad </h4>
<p>Este principio reconoce a los niños, las niñas, los adolescentes, y mayores de 18 años,
 con sus derechos inobservados, amenazados, o vulnerados sus familias y/o redes vinculares de apoyo, 
 como individuos multidimensionales en los que interactúan aspectos biológicos, psicológicos y sociales
  que determinan su desarrollo integral. Para dar respuesta a este principio, la Fundación Salvado Sueños
   trabajara conjuntamente con las defensorías de familia ICBF, para poder llevar a cabo las acciones necesarias,
   de coordinación y articulación del SNBF y demás actores sociales, para garantizar el reconocimiento, la garantía,
   la protección y el restablecimiento de sus derechos, así como la prevención de su amenaza o vulneración. <br><br>
   Esta concepción hace que se plantee un marco de trabajo en el que se le de participación a diversos agentes
    socializantes que hacen posible brindar una atención integral. Por un lado, el equipo de trabajo cuenta
    con profesionales de del área de salud como Psiquiatría, nutrición, medicina general, enfermería, psicología,
    trabajo social, especialistas de área (pedagogía) y formadores que intervienen en los diferentes niveles 
    de causa del problema aportando sus conocimientos específicos pero comprendiendo el fenómeno desde una 
    perspectiva transdisciplinaria, es decir, permitiendo que el centro de la reflexión sea la realidad de los niños,
    niñas, adolescentes y sus familias y no las fronteras disciplinares. Por otro lado, se desarrolla un trabajo
    interinstitucional impactando en las áreas de derechos al realizar gestiones coordinadas con profesionales
    de diferentes instituciones públicas o privadas. <br><br>
    Por medio de la articulación interdisciplinaria e intersectorial, se propende por brindar a los niños,
    niñas, adolescentes, mayores de 18 años, familias y/o redes vinculares de apoyo del programa, 
    una atención integral, desde sus particularidades y contextos, teniendo en cuenta aspectos biológicos,
    psicológicos y sociales, buscando el desarrollo de capacidades y habilidades y la superación de dificultades
    para una mejor calidad de vida.
</p>
<h4 id="individualidad">Individualidad</h4>
<p>Este principio se fundamenta en reconocer la particularidad y peculiaridad de cada niño, niña, 
adolescentes y mayores de 18 años, consiste en confiar en las capacidades y habilidades que vayan
adquiriendo dentro de su diario vivir, en el que fomenten una autonomía, y desarrollen
la capacidad de tomar decisiones; además la fundación salvado sueños les brindara un ambiente
seguro y cómodo, promoviendo que la atención se fundamente en el reconocimiento y respeto por la diferencia, 
en un contexto de igualdad, equidad y especialización en la atención específica de acuerdo
a las situaciones singulares de cada niño, niña, adolescente, y mayores de 18 año, sus familias
y comunidades considerando la dinámica compleja del entramado de interrelaciones. <br><br>
Lo anterior se puede evidenciar en las intervenciones y respectivas evoluciones que se realizan 
desde cada área teniendo en cuenta las características especiales de cada niño, niña, adolescente
y mayores de 18 años, es decir desde el Enfoque Diferencial.
</p>
<h4 id="corresponsabilidad">Corresponsabilidad</h4>
<p>La Fundación Salvando Sueños en conjunto con el Instituto Colombiano Bienestar Familiar
tiene la responsabilidad de acoger a los niños, niñas, adolescentes o mayores de 18 años en
situación de riesgo psicosocial, inobservancia, vulneración y brindar una cálidad de vida, y aun ambiente
sano en condiciones de dignidad y goce de todos sus derechos; la calidad de vida es esencial para el desarrollo 
integral de nuestros beneficiarios, asegurando la concepción, protección, alimentación nutritiva  y equilibrada,
acceso a los servicios de salud, educación, vestuario adecuado, y recreación; así mismo se prestara una atención 
especializada y acorde a su diagnóstico desde las diferentes disciplinas. <br><br>
Desde el modelo de atención de la Fundación Salvando Sueños, las familias son consideradas gestoras de su propio 
desarrollo con firmes deseos de mejorar su calidad de vida, poniendo al servicio de sí mismas su capacidad
de transformar y /o reconstruir sus proyectos de vida familiar. Por eso, al interior del programa se realiza 
intervenciónes de apoyo en donde se vincula a las familias o redes vinculares de los niños, niñas y adolescentes,
orientándolos y educándolos frente a la importancia de garantizar y proteger los derechos de sus hijos, minimizando
situaciones de riesgos existentes en el entorno familiar y social, reflexionando frente a sus roles como cuidadores
y generando cambios que permitan mejorar sus condiciones de vida. <br><br>
Así mismo, en el programa, se desarrollan algunas estrategias para fortalecer el principio de
corresponsabilidad como las escuelas de familias, seguimientos familiares, actividades pedagógicas, 
construcción de redes sociales, comunitarias e institucionales, referenciación a servicios o rutas de atención
que apuntan a la articulación de todos estos actores sociales y que dan cumplimiento a este principio. 
</p>
