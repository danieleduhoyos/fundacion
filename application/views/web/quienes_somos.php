<h3>SÍNTESIS</h3>
<p>El objetivo Institucional es brindar protección y propiciar el desarrollo integral
 de este tipo de población y para ello se diseñará, implementará y ejecutará ideas, propuestas,
 proyectos y programas, que posibiliten el mejoramiento de la calidad de vida y bienestar en cada 
 uno de los nnnaaa. Es relevante mencionar que la convivencia, aceptación y respeto por los demás. 
 Además de la inclusión y participación de la familia son aspectos trascendentales para la Fundación. <br><br>
 En cuanto al modelo de atención institucional se reseña que cada caso presenta características propias
 que merecen ser reconocidas, identificadas, y diagnosticadas entre ellas, patología, edad, género,
 procedencia entre otras. De igual forma se garantiza que los procesos de acogida, proyección, seguimiento,
 acompañamiento e intervención se hacen de forma individual y grupal, según el caso, y para eso se cuenta 
 con instalaciones físicas acordes a las expectativas sociales. Además del trabajo interdisciplinario del personal
 profesional capacitado que garantice el correcto restablecimiento de los derechos humanos. <br><br>
 Dando continuidad a lo anterior la Fundación Salvando Sueños, cuenta con un equipo de Talento Humano
 conformado por: el área de salud en donde se encuentra Psiquiatra, Nutricionista, Medico General, Odontóloga,
 turnos de auxiliares de enfermería, y Psicología,  quienes se encargan de velar por la  promoción de la salud
 física y mental  y prevención de enfermedades, a través del acompañamiento pertinente en lo referente a las
 respectivas valoraciones y controles, así como también formulación y suministro de medicamento y una alimentación
 balanceada acorde a la minuta establecida por el ICBF.<br><br>
 También conforman dicho equipo de trabajo el área de pedagogía en cabeza del especialista del área,
 turnos de formadores, y Trabajo Social, quienes hacen parte del equipo interdisciplinario, área administrativa 
 conformada por: los coordinadores de la modalidad, salud ocupacional, auxiliar administrativa y un área de
 servicios generales. <br><br>
 En este orden de ideas, como mecanismos que faciliten y favorezcan el satisfactorio restablecimiento 
 de los derechos de los niños, niñas, adolescentes y mayores de 18 años adscritos a la Fundación Salvando
 Sueños, se ha implementado como estrategia para la preparación de su egreso y/o proceso de transición tanto
 para los beneficiarios que se encuentran en restablecimientos de derechos como de adoptabilidad,  diseñar, 
 adaptar y ejecutar proyectos con ajustes razonables, como herramientas técnicas de apoyo entre los cuales
 se encuentran: <br><br>
 “Hábitos de Vida Saludable”, “Habilidades para la Vida”, “Proyecto de Vida” “Promoción de la salud 
 y prevención de enfermedades con beneficiarios y familia”, “Promoción salud sexual con beneficiarios
 y familia” “Prevención consumo de alcohol y sustancias psicoactivas”, “Educación alimentaria con beneficiarios
 y familia”, “Desarrollo de potenciales básicas y específicas”, “Auto-reconocimiento de los beneficiarios como
 sujetos de derechos  externos de los mismos”, “promoción de los Derechos y prevención de su vulnerabilidad con
 beneficiarios y familia”, prevenir y detectar el maltrato y abuso sexual con beneficiarios y familias”, “prevención
 peores formas de trabajo con beneficiarios y familia”, “prevención explotación económica y sexual con beneficiarios
 y familia”, “prevención del reclutamiento por parte de grupos armados organizados al margen con beneficiario
 y familia”, escuela de padres “creciendo y aprendiendo junto” y “manualidades kariñocitas”. <br><br>
 Igualmente son desarrolladas propuestas de participación como lo son el Pacto de Convivencia, buzón de sugerencias
 y encuestas de satisfacción. Además de herramientas de seguimientos y estudios de caso y cuántos aspectos 
 relevantes merecen ser atendidos y en general todos los aspectos que merezcan ser atendidos. Como mecanismo de
 control y verificación institucional la Fundación Salvando Sueños, cuenta con un Sistema de Evaluación
 Institucional, con el cual se identifica qué logros y propósitos se han alcanzado y que permitan implementar
 planes de mejora. <br><br>
 De igual forma se da a conocer y se describen programas y estrategias en relación a la gestión 
 de riesgo y salud laboral como aspectos trascendentales que merecen la atención y oportuno reconocimiento 
 institucional. Así mismo el Plan de atención y prevención de desastres, estrategias, planes, proyectos
 ambientales, y sistema de gestión seguridad informática. <br><br>   
</p>
<h3>OBJETIVOS</h3>
<h4>General</h4>
<p>Brindar protección, siendo parte activa en la restitución y la garantía de los derechos 
a la población con discapacidad mental psicosocial con sus derechos Amenazados, Inobservados
o Vulnerados, haciendo énfasis en su desarrollo integral e inclusión socio familiar.
</p>
<h4>Específicos</h4>
<ul>
    <li><p>Lograr que los niños, niñas, adolescentes y mayores de 18 años con discapacidad mental
     y psicosocial desarrollen habilidades y destrezas generales que les permitan mejorar su calidad
     de vida e inclusión integral a través de herramientas terapéuticas especializadas y físicas.</p>
    </li>
    <li><p>Apoyar y motivar a los niños, niñas, adolescentes y mayores de 18 años con discapacidad mental
    y psicosocial a direccionar un proyecto de vida que les permita un crecimiento espiritual y emocional,
    mediante el desarrollo y reforzamiento de las capacidades y habilidades, orientadas de acuerdo a sus potenciales.</p>
    </li>
    <li><p>Orientar a los miembros de la familia o a las personas responsables de los niños, niñas, adolescentes
    y mayores de 18 años en situación de discapacidad mental y psicosocial, para dar continuidad al proceso
    integral de restablecimiento de derechos.</p>
    </li>
</ul><br>
<h3> MISIÓN </h3><br>
    <p>La Fundación Salvando Sueños  nace como una entidad sin ánimo de lucro,
       se encuentra ubicada en la ciudad de Popayán Departamento del Cauca, 
       y brinda atención integral especializada en modalidad de internado a niños(as), 
        adolescentes y mayores de 18 años con discapacidad mental y psicosocial,  
        con sus derechos Amenazados, inobservancia  o Vulnerados donde se les  restablecen y garantiza
        sus Derechos y se promueve el fortalecimiento de factores que posibilitan el mejoramiento
        de su calidad de vida, física, mental, familiar y social, teniendo en cuenta  las diversidades
        e inequidades existentes en la realidad, como la diversidad sexual, territorial y discapacidad; 
        de tal modo que a través de programadas de intervención integral se logre contribuir  a la formación 
        y búsqueda de sus potencialidades, a través de un proyecto de vida que permita la integración e inclusión
        en su medio familiar social y cultural.
    </p><br>
<h3> VISIÓN </h3><br>
    <p>La Fundación Salvando Sueños se proyecta para el año 2020, como una institución pionera, 
        líder y reconocida por su excelente desempeño en la identificación, diagnóstico, acogida, 
        intervención, proyección y preparación para el egreso de los niños(as), adolescentes y 
        mayores de 18 años con discapacidad mental psicosocial, cuyos derechos han sido amenazados, 
        inobservados o vulnerados;  por medio de un tratamiento especializado, cuidado especial en salud, 
        educación, orientación y apoyo a los miembros de la familia o redes de apoyos, teniendo en cuenta 
        la diversidad sexual, territorial y discapacidad de los sujetos titulares de derechos.  
    </p>
