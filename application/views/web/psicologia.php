<h4>Valoración Psicológica</h4>
<p>Una vez el niño, niña, adolescente y mayor de 18 años ingresa como
beneficiario de la Fundación Salvando Sueños, el acompañamiento psicológico tiene como objetivo
principal contribuir a mejorar su calidad de vida y por consiguiente aportar en la etapa de transición
que inicia dentro de su curso de vida. Es así como se parte de un protocolo general que inicia estableciendo
el respectivo rappot con el fin de generar empatía y confianza, e indagar sobre aspectos generales que permitan
conocer más a fondo la historia de vida del beneficiario realizando una valoración inicial 
interdisciplinaria de ingreso. <br><br>
Adicionalmente, se lleva a cabo observación directa y comportamental para establecer el estado cognitivo,
social conductual y emocional con el que ingresa el beneficiario, por lo general utilizando como herramienta
de apoyo en una prueba cognitiva acorde a las capacidades para el caso de los beneficiarios más funcionales.
