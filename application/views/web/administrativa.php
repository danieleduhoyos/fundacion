<h4>Secretaria</h4>
<p>Una secretaria o asistente administrativa, que trabaja para un gerente desempeña una serie de tareas similares
a las de una asistente de oficina. Sin embargo, el enfoque del trabajo es de los proyectos de su jefe y no de todo
el departamento o de todos los empleados de la compañía. Las tareas incluyen el manejo de las comunicaciones verbales
y escritas, la preparación de documentos, la organización de la oficina y la gestión de los proyectos. El gerente
a quién atiende la asistente es típicamente de nivel medio quién supervisa a un equipo de empleados, contrario a 
un ejecutivo de alto nivel.<br><br>
