<h4>Representante Legal</h4>
<p>El representante legal de una empresa es una persona que actúa en nombre de esa empresa para realizar diversos
trámites. Puede asumir compromisos y tomar decisiones que serán atribuidas a la empresa como persona jurídica. 
La representación legal otorga a quien la ejerce la facultad de ocuparse de negocios, obligaciones y hasta
derechos de su representado, de acuerdo con las condiciones acordadas en el momento de crearse la representación.<br><br>
