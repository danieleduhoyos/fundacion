<h4>Sistema de Gestión</h4>
<p>Serie de procesos, acciones y tareas que se llevan a cabo sobre un conjunto de elementos (personas,
procedimientos, estrategias, planes, recursos, productos, etc.) para lograr el éxito sostenido de 
una organización, es decir, disponer de capacidad para satisfacer las necesidades y las expectativas
de sus clientes o beneficiarios, trabajadores y de otras partes interesadas a largo plazo y de un modo
equilibrado y sostenible. <br><br>
