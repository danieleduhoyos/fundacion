<h3>Elaborado por Todos</h3><br>
<h4>Observación Directa y Comportamental del Beneficiario</h4>
<p>La cual tiene como objetivo como su nombre lo indica, observar el comportamiento
  y la actitud del beneficiario durante su proceso de adaptación, con la cual será establecido desde las 
  diferentes áreas el manejo o protocolo interventivo especifico a implementar durante el proceso del
  beneficiario en la Fundación.   Es importante mencionar que, dado el caso de los niños, niñas, adolescentes
  y mayores de 18 años con Diagnósticos, que, por su alta disfuncionalidad, no permiten la realización de la
  entrevista y la aplicación de las pruebas, se lleva a cabo la aplicación de técnicas y terapias interventiva
  propias acordes a la patología que presente el beneficiario.
</p>
