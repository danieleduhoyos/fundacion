    <h4 id="cambiar" data-toggle="collapse" data-target="#seguimiento" aria-expanded="false" aria-controls="seguimiento"><i class="fas fa-plus-circle">Seguimiento Familiar</i></h4>
    <div id="seguimiento" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
        <p>Durante este seguimiento se establecen compromisos con la familia o red vincular de apoyo
        frente a la implementación y mantenimiento de condiciones favorables para el bienestar del niño, niña o
        adolescente, en su medio familiar. Se orienta a la familia o red vincular de apoyo, para que se garantice
        la continuidad de la vinculación del beneficiario, a los servicios de salud, educación, recreación, artístico
        y cultural entre otros. Se orienta a la familia para continuar fortaleciendo y reforzando el mantenimiento del 
        aprendizaje adquirido durante su proceso en la Fundación Salvando Sueños, entre los cuales se encuentran: 
        los hábitos de vida saludable, habilidades para la vida, proyecto de vida acorde a sus características especiales,
        prevención de consumo de sustancias psicoactivas, reconocimientos de sus derechos y evitar la vulneración
        de los mimos, prevención en temática relacionadas con el conflicto armado, explotación sexual, trabajo infantil
        forzado, abuzo sexual, manejo responsable de la sexualidad, y promoción de la salud y prevención de enfermedades.
        </p>
        <p>Estas son interacciones presenciales a cargo de cualquier profesional del equipo Interdisciplinario, en especial del
          trabajador social con una familia específica o acudiente en cualquier momento y fase del proceso (aceptación,
          permanencia o proyección). Suelen desarrollarse en la unidad de servicio mediante solicitud del equipo técnico 
          o de la misma familia y se hacen con la finalidad de acercarla a la Institución, reflexionar sobre aspectos
          parentales o del proceso, confrontar o informar sobre una situación específica, referenciar a algún servicio
          o institución, establecer o renovar compromisos frente al proceso o contener en situaciones difíciles.
          </p>
        </div>
    </div>
