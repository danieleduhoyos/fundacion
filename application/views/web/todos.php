<div id="accordion">
    <h4 id="cambiar" data-toggle="collapse" data-target="#intervencion" aria-expanded="false" aria-controls="intervencion"><i class="fas fa-plus-circle"> Intervención y Proyección</i></h4>
    <div id="intervencion" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
          <p>Después de que el niño, niña, adolescente y mayor de 18 años en situación de Discapacidad mental psicosocial
           se siente en un ambiente propio, familiar y demuestra disposición de permanencia  institucional durante su periodo
           de restablecimiento de derechos, se da paso a una habilitación funcional de segunda etapa donde los beneficiarios
           adquieran una mejor recepción de estímulos externos dando inicio a su proceso de aprendizaje significativo, generando
           acciones de fortalecimiento de potencialidades, habilidades y destrezas. En esta fase se desarrollan 
           acciones interdisciplinarias terapéuticas e incluyentes para que los niños, niñas, adolescentes y mayores
           de 18 años en situación de Discapacidad mental psicosocial, exploren, avancen y desarrollen sus habilidades
           funcionales y sociales definidas en el plan de atención integral. Durante su estadía en la Institución,
           cada niño, niña, adolescente y mayor de 18 años, es intervenido en crisis, a nivel individual, y grupal acorde
           a sus necesidades; individualmente en especial desde el enfoque Cognitivo Conductual, y grupal contando con herramienta 
           el Juego Cooperativo Prosocial cuyo objetivo es incrementar las conductas asertivas y disminuir las inadecuadas.
            Como también, diariamente todos cuentan con reforzamiento afectivo y positivo.
          </p>
        </div>
    </div>
</div>
<div id="accordion">
    <h4 id="cambiar" data-toggle="collapse" data-target="#referenciacion" aria-expanded="false" aria-controls="referenciacion"><i class="fas fa-plus-circle"> Referenciación</i></h4>
    <div id="referenciacion" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
        <p>Es una orientación para el acceso a servicios institucionales en diferentes áreas de derecho o rutas de atención 
        en el caso de personas que requieren de una atención especializada que no corresponde a los servicios ofertados por
        la fundación, tales como documentación legal, salud, educación, violencia intrafamiliar, abuso sexual, maltrato 
        infantil, víctimas (de acuerdo con la ley 1448 de 2011), consumo de sustancias psicoactivas en etapa de superación etc.
        </p>
        <p>Se orienta al niño, niña, adolescente o mayor de 18 años y su familia y/o red vincular de apoyo,
        sobre los servicios interinstitucionales y redes de apoyo con los que cuenta.
        </p>
        </div>
    </div>
</div>
