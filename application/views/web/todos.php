    <h4 id="cambiar" data-toggle="collapse" data-target="#individual" aria-expanded="false" aria-controls="individual"><i class="fas fa-plus-circle">Seguimiento Individual</i></h4>
    <div id="individual" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
        <p>Se realiza para retroalimentar al niño, niña, adolescente o mayor de 18 años, sobre los
