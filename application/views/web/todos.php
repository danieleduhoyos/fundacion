    <div id="plan" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
          <p>Con base en el diagnóstico integral, se propone el plan de atención individual- PLATIN con
          la participación de todo el equipo técnico. Este plan de acción orienta las intervenciones a ejecutar
          por las profesionales con la población participante, las metas y los tiempos en que se proyecta el alcance 
          de dichas metas. <br><br>
          Las anteriores actuaciones por las diferentes áreas, se desarrollan en un ambiente de respeto y confianza
          para que los niños, niñas, adolescentes  y mayores de 18 años, al igual que sus familias, se sientan acogidas, 
          protegidas y a su vez se les mantenga informados sobre su proceso, con el fin  de que puedan ejercer su derecho
          a la participación; a los beneficiarios que pertenecen a grupos étnicos o se identifiquen con una tendencia sexual 
          determinada, se les garantizara el derecho a la diversidad en todos sus aspectos como lo reza en la Ley.     
          </p>
