           que a partir del diagnóstico integral se realiza el reconocimiento y descripción de la familia como un sistema,
           por tanto, se aborda las características del beneficiario como de su familia en procesos de interacción y reciprocidad. <br><br>
           Igualmente, integra información proporcionada por la autoridad administrativa competente, equipo técnico
           interdisciplinario y la autoridad tradicional (en el caso de personas pertenecientes a grupos étnicos) 
           para la comprensión del caso. Incluye un resumen de los factores de generatividad y vulnerabilidad identificados.
          </p>
