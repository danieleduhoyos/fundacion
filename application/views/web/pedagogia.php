<h4>Valoración Pedagógica</h4>
        <p>Desde esta área se realiza el protocolo general, con el fin de observar las competencias y
        destrezas escolares de los niños, niñas, adolescente y mayores de 18 años, se realiza la valoración
        de competencias educativas y se determina el grupo pedagógico en el que se ingresa el beneficiario 
        según las necesidades educativas especiales que presente.
        </p>
<div id="accordion">
    <h4 id="cambiar" data-toggle="collapse" data-target="#deporte" aria-expanded="false" aria-controls="deporte"><i class="fas fa-plus-circle">Actividades Deportivas y/o Artísticas</i></h4>
    <div id="deporte" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
        <p>Son encuentros grupales dirigidos generalmente por el especialista de área o invitados
        especiales con competencias para la enseñanza de pintura, manualidades, deporte, danza, recreación
        o teatro. Estas actividades, además de fortalecer competencias técnicas, trabajan aspectos como
        la cooperación, el trabajo en equipo, el control de impulsos, manejo de reglas, la promoción de la
        recreación y la sana competencia.
        </p>
        </div>
    </div>
</div>
<div id="accordion">
    <h4 id="cambiar" data-toggle="collapse" data-target="#educativo" aria-expanded="false" aria-controls="educativo"><i class="fas fa-plus-circle">Actividades para la Vinculación o Permanencia en el Sistema Educativo Formal</i></h4>
    <div id="educativo" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
        <p>En este proceso, es importante resaltar las estrategias utilizadas para motivar el ingreso o la permanencia
          en el sistema educativo formal, captar su interés hacia el mismo y lograr que le encuentren sentido a los
          aprendizajes que están adquiriendo. Entre estas estrategias se encuentran: Encuentros individuales o grupales
          para favorecer la sensibilización, motivación y reflexión sobre la importancia de la educación formal.
          Compromisos por escrito para retomar actividades académicas.
        </p>
        </div>
    </div>
</div>
<div id="accordion">
    <h4 id="cambiar" data-toggle="collapse" data-target="#proyecto" aria-expanded="false" aria-controls="proyecto"><i class="fas fa-plus-circle">Proyecto de vida</i></h4>
    <div id="proyecto" class="collapse" aria-labelledby="headingTwo" data-parent="#accordion">
        <div class="card-body">
        <p>En este orden de ideas el proyecto de vida es abordado desde el método por proyectos que se adelanta por 
        parte del área de pedagogía con el desarrollo de competencias básicas, y el Equipo Psicosocial, utilizando como 
        herramienta el proyecto “Manualidades Kariñocitas”, con el cual se pretende abarcar gran parte de la población
        conductual acorde a sus características y gustos, tratando de alcanzar el objetivo general de canalizar las emociones
        y paralelamente construir Proyecto de Vida a corto y mediano plazo a través de las manualidades, con apoyo terapéutico
        durante la sesión. <br><br>
        En lo que respecta al acompañamiento de la población de niños, niñas, adolescentes y mayores de 18 años clínicos
        es a partir de la Estimulación Sensoperceptiva, proyecto “Capacidad en la discapacidad” con el objetivo de seguir
        potenciando las áreas del desarrollo cognitivo, emocional, social y conductual a través de la inclusión social 
        formativa y la Estimulación Sensoperceptiva de los niños, niñas, adolescentes y mayores de 18 años clínicos durante
        su proceso en la Fundación Salvando Sueños.
        </p>
        </div>
    </div>
